	<p>
		Akibat Pandemi COVID-19 yang sedang terjadi diseluruh dunia mengakibatkan dampak pada perekonomian masyarakat terutama di Indonesia. Dampaknya terjadi kepada para pedagang kecil yang mengaku dagangannya sepi sehingga mengalami penurunan pendapatan.
	</p>
	<p>
		Hal ini terjadi kepada para pedagang disekitar kelurahan Gayungan RW 03 Surabaya. Warung-warung yang biasanya selalu ramai oleh para pembeli yang mengantre untuk membeli kini warung-warung tersebut sepi bahkan tidak ada satupun pembeli yang berkunjung ke warung tersebut. 
	</p>
	<p>
		Jum’at (10/7), saya bertemu dengan Ibu Karmini (35) seorang pedagang makanan yang sedang menganggur di warungnya karena tidak ada pembeli. Ibu Karmini mengaku bahwa pendapatannya turun drastis hingga hampir 85 persen.
	</p>
	<p>
		Meskipun dalam keadaan seperti ini warung makan Ibu Karmini tetap buka seperti biasanya dan tetap antisipasi mengikuti anjuran protokol kesehatan dari pemerintah yaitu menggunakan masker, menjaga jarak dan sebelum masuk ke warung diharapkan untuk mencuci tangan terlebih dahulu.
	</p>
	<blockquote class="blockquote">
		<p class="mb-0" style="font-size: 1.0rem;">
			<em>
				“Biasanya saya mendapat penghasilan 400-450 ribu per hari sekarang pendapatan mentok cuma 50-100 ribu per harinya”
			</em>
		</p>
		<footer class="blockquote-footer">
			ungkap Ibu Karmini.
		</footer>
	</blockquote>
	<p>
		Akibat adanya pandemi, Ibu Karmini mengaku sedih karena dagangannya tidak pernah terjual habis dan mengalami kendala perekonomian yang tidak stabil karena dagangannya yang sepi.
	</p>
	<p>
		Ibu Karmini mengaku bahwa beliau mendapatkan bantuan subsidi dari pemerintah yang berupa sembako. Beliau berterima kasih dengan adanya bantuan sembako dari pemerintah, karena menurut beliau bantuan tersebut dianggap cukup untuk memenuhi kebutuhan pangan untuk sehari-hari untuk keluarganya.
	</p>
	<blockquote class="blockquote">
		<p class="mb-0" style="font-size: 1.0rem;">
			<em>
				“Saya berharap agar wabah ini segera berakhir dan semoga saya dan juga para pedagang lainnya bisa bejualan seperti sedia kala agar bisa memenuhi kebutuhan sehari-hari”
			</em>
		</p>
		<footer class="blockquote-footer">
			ujar Ibu Karmini.
		</footer>
	</blockquote>
	<hr>
	<p>
		Tidak hanya Ibu Karmini saja yang mengalami penurunan pendapatan penjualannya dimasa pandemi ini, Pak Joko (38) pedagang bakso keliling juga mengalami hal yang serupa dan banvak mengalami kerugian karena dagangannya tidak pernah terjual habis padahal Pak Joko sudah mengurangi porsi jualannya tidak seperti biasanya.
	</p>
	<blockquote class="blockquote">
		<p class="mb-0" style="font-size: 1.0rem;">
			<em>
				“Padahal saya memiliki tanggungan untuk mencukupi kebutuhan istri dan dua anak saya yang masih sekolah, walaupun sekolah daring tapi spp tetap harus dibayar”
			</em>
		</p>
		<footer class="blockquote-footer">
			ungkap Pak Joko.
		</footer>
	</blockquote>
		Atas peristiwa ini Ibu Karmini, Pak Joko dan para pedagang lainnya berharap agar wabah yang sedang terjadi di dunia segera berakhir agar bisa menjalani kehidupan normal seperti sedia kala dan berharap agar perekonomian kembali stabil agar bisa terus melanjutkan hidup. 
	</p>
	<p>
		Semoga semua masyarakat selalu diberikan kesehatan dan tetap menjaga jarak agar terhindar dari virus CORONA dan semoga virus CORONA segera berakhir dan semua masyarakat bisa menjalankan aktivitas kehidupan normal seperti sedia kala.
	</p>
