  <p>
    Surabaya sedang mengalami kondisi yang sulit di masa pandemi COVID-19 ini. Sebanyak lebih dari 7.000 kasus terkonfirmasi tersebar di lima bagian wilayah Kota Surabaya. Jumlah tersebut menjadikan Surabaya sebagai penyumbang kasus COVID terbesar di Jawa Timur.
  </p>
  <p>
    Beragam upaya telah dilakukan untuk meminimalisir kenaikan kasus COVID ini. Mulai dari Pembatasan Sosial Berskala Besar yang sempat diperpanjang, hingga kemudian kembali dengan new normal pada awal Juni lalu. Namun hal tersebut tampaknya tidak memberikan pengaruh yang signifikan. Banyaknya jumlah kasus COVID menyebabkan Surabaya menyandang status zona hitam. Hal ini sempat menjadi perbincangan di masyarakat luas.
  </p>
  <p>
    Hal ini seharusnya memberikan kewaspadaan bagi masyarakat Surabaya. Kondisi di Surabaya yang kini memasuki fase new normal sebaiknya tidak membuat masyarakat lengah akan keberadaan virus CORONA yang sejatinya bisa kita temui dimana saja meskipun tidak tampak secara kasatmata. Karena virus CORONA berpotensi menyerang siapapun tidak peduli siapapun dan berapa umurnya.
  </p>
  <p>
    Dilansir dari <a href="https://www.bbc.com/indonesia/indonesia-51411440" target="_blank">BBC Indonesia</a>, sebagian besar pasien yang terjangkit virus CORONA adalah golongan usia lanjut dan laki-laki di China, ujar satu lembaga penelitian kesehatan. Sementara itu, data yang sama menunjukkan kasus anak-anak terbilang jarang.
  </p>
  <p>
    Virus CORONA tentu menjadi momok bagi masyarakat. Menjadi sebuah dilema bagi sebagian besar masyarakat hanya untuk sekedar keluar rumah. Orang dewasa tentu bisa berfikir bagaimana harus bersikap. Namun pernahkah kita berpikir bagaimana tanggapan atau sudut pandang anak-anak mengenai pandemi dan virus CORONA ini?
  </p>
