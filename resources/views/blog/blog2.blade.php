  <p>
    Jumat (10/7) lalu, saya berkesempatan untuk mendatangi sebuah perkampungan di Kelurahan Gayungan, Kecamatan Gayungan, Surabaya, tepatnya di area RW 03. Saat saya mendatangi perkampungan tersebut suasananya tidak terlalu ramai, sepertinya warga sedang sibuk di dalam rumah dan sebagian lainnya sedang pergi bekerja. Namun saya masih melihat segerombolan anak-anak berusia 5-10 tahun asik bermain di dekat bangunan Taman Kanak-kanak yang juga berdekatan dengan masjid. 
  </p>
  <p>
    Mereka tampak asik bermain dan ada yang bersepeda tanpa menggunakan masker. Saya agak terkejut melihatnya. Lalu, saya mencoba untuk mendekati beberapa orang anak di sana. Saya berkenalan dan mengobrol sedikit dengan mereka. Singkat cerita saya bertanya mengenai apa yang mereka ketahui tentang virus CORONA yang sedang mewabah ini. Kebanyakan dari mereka mengerti atas apa yang sedang terjadi, apa itu virus CORONA dan bagaimana bentuknya, serta mengapa sekolah dilakukan secara daring (online). Mereka mengetahui informasi tersebut dari himbauan sekolah, dari orang tua mereka, serta tayangan animasi di YouTube. Mereka bahkan mengetahui adanya korban-korban COVID yang harus dirawat di rumah sakit juga korban yang meninggal. Beberapa dari mereka mengetahui hal tersebut dari tayangan televisi. Mereka juga mengaku mengetahui ada warga di lingkungan mereka yang terpaksa dijemput oleh tenaga medis.
  </p>
