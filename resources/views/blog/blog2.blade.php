  <p>
    Sebagian dari mereka mengakui bahwa mereka takut dengan adanya virus CORONA, namun ketika ditanya mengapa mereka tidak mengenakan masker saat berada di luar rumah mereka menjawab “Gak suka, bikin sesak nafas.” Saya tertawa kecil mendengarnya. Namun begitu, mereka mengetahui dan masih menerapkan sebagian protokol kesehatan yang telah dianjurkan. Mereka mencuci tangan dengan sabun atau cairan pembersih hand sanitizer sepulang bermain.
  </p>
  <p>
    Saya juga tertarik untuk kemudian mewawancarai orang tua dari salah satu anak tersebut. Bu Ifa (29) bercerita sedikit mengenai bagaimana keseharian putrinya yang tengah duduk di bangku sekolah dasar, yaitu SDN Ketintang 1 Surabaya selama masa pandemi ini. Sebagai seorang ibu rumah tangga sekaligus pekerja, Bu Ifa mengaku cukup kewalahan dalam mengawasi anaknya yang bersekolah secara daring, ia terkadang kesulitan membagi waktu antara pekerjaannya di luar rumah dan mengurus anaknya di rumah.
  </p>
  <p>
    Bu Ifa sedikit-banyak mengajarkan pada anaknya mengenai pentingnya menjaga kebersihan selama masa pandemi ini, sebisa mungkin ia juga memberikan asupan makanan yang bernutrisi dan memberikan vitamin secara berkala untuk anaknya agar terlindungi dari virus. Namun anehnya, ketika ditanya mengapa anaknya tidak mengenakan masker saat bermain di luar rumah ia menjawab “kalau masih di dekat rumah, ndak pakai (masker)”. Padahal kita semua tau bahwa virus CORONA ini tidak kasatmata, sehingga kita juga tidak bisa memastikan keberadaan virus itu sendiri. Maka seharusnya kita mengenakan masker selama berada di luar rumah, tidak peduli seberapa dekat jaraknya.
  </p>
